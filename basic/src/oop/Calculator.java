package oop;

public class Calculator {
	
	
	
	public Calculator() {
	
	}
	
	int add(int num1, int num2){
	
		int result=num1+num2;
		return result;
	}

	
	int subtract(int num1, int num2){
		
		int result=num1-num2;
		return result;
	}
	
	
	int multiply(int num1, int num2){
		
		int result=num1*num2;
		return result;
		
		
	}
	
	
	
	double divide(double num1, double num2){
		
		double result= num1  / num2;
		return result;
		
		
	}
	
	
	
	
	
	
	
}
